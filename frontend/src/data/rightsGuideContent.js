function collectLeaves(nav, out = []) {
  for (const node of nav) {
    if (node.body) {
      out.push(node);
    }
    if (node.children) {
      collectLeaves(node.children, out);
    }
  }
  return out;
}

export function getAllLeafSections() {
  return collectLeaves(RIGHTS_GUIDE_NAV, []);
}

export function getSectionById(id) {
  const leaves = getAllLeafSections();
  return leaves.find((s) => s.id === id) || null;
}

export function getAllSectionIds() {
  return getAllLeafSections().map((s) => s.id);
}
